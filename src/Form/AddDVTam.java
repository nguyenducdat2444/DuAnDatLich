    private void showTableDichVu(ArrayList<Dich_Vu> lists) {
        dtmDichVu.setRowCount(0);
        AtomicInteger index = new AtomicInteger(1);
        lists.forEach(s -> dtmDichVu.addRow(new Object[]{
            index.getAndIncrement(),
            s.getTen_DV(), s.getThoiGianDV(), s.getGia_Tien()
           
        }));
    }

