    private void btnThemDvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemDvActionPerformed
        int i = JOptionPane.showConfirmDialog(this, "Bạn yêu muốn gì");
        if (i == JOptionPane.YES_OPTION) {
            viTri = tblDichVu.getSelectedRow();
            if (viTri == -1) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn dịch vụ cần thêm");
                return;
            } else {
                int id  = dvRepo.getAll().get(viTri).getId();
                String ma = dvRepo.getAll().get(viTri).getMa_DV();
                String ten = dvRepo.getAll().get(viTri).getTen_DV();
                String tg =String.valueOf(dvRepo.getAll().get(viTri).getThoiGianDV());
                double gia =dvRepo.getAll().get(viTri).getGia_Tien();
                dvTam.add(new DvTam(id, ma, ten, tg, gia));
                dl.showTableAddDv(dvTam.getAll());
                
                dispose();
                
            }

        }
    }//GEN-LAST:event_btnThemDvActionPerformed

